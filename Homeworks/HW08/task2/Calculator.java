package HW08.task2;

public class Calculator {
    public static int sumNumbers(int firstNum, int secondNum) {
        return firstNum + secondNum;
    }

    public static double sumNumbers(double firstNum, double secondNum) {
        return firstNum + secondNum;
    }

    public static int subNumbers(int firstNum, int secondNum) {
        return firstNum - secondNum;
    }

    public static double subNumbers(double firstNum, double secondNum) {
        return firstNum - secondNum;
    }

    public static int multiplyNumbers(int firstNum, int secondNum) {
        return firstNum * secondNum;
    }

    public static double multiplyNumbers(double firstNum, double secondNum) {
        return firstNum * secondNum;
    }

    public static int divNumbers(int firstNum, int secondNum) {
        return firstNum / secondNum;
    }

    public static double divNumbers(double firstNum, double secondNum) {
        return firstNum / secondNum;
    }
}
